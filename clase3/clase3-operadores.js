//Suma
console.log(5+4);

//Resta 
console.log(10-5);

//Multiplicacion 
console.log(3*4);

//Division 
console.log(10/2);

//Modulo
console.log(10%2);

//Incremento
let numero = 6;
console.log(++numero);

//Decremento 
let numero2 = 10;
console.log(--numero2);


//OPERADORES DE COMPARACION
//La respuesta es un dato booleano (true o false)
//Comparacion simple
console.log(5 == '5');

//Comparacion estricta
console.log(5 === '5');

//Desigualdad 
console.log(5 != '5');

//Desigualdad estricta
console.log(5 !== '5');

//Mayor que 
console.log(10 > 6);

//Mayor o igual que
console.log(6 >= 6);

//Menor que 
console.log(7 < 7);

//Menor o igual que 
console.log(7 <= 7);



//OPERADORES LOGICOS
//AND
console.log(true && true);
console.log(true && false);

//OR
console.log(true || true);
console.log(true || false);



//CONCATENACION
let nombre = 'Mariana';
let apellido = 'Ampudia';
console.log(nombre + ' ' + apellido);
